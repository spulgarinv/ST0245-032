public class Abeja {

    Double x;
    Double y;
    Double z;

    public Abeja(Double x, Double y, Double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {

        return x;
    }

    public double getY() {

        return y;
    }

    public double getZ() {

        return z;
    }
}
